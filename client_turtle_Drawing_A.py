import turtle

turtle.setup(800,600)
window=turtle.Screen()
window.title('Drawing an A')

the_turtle=turtle.getturtle()
the_turtle.penup()
the_turtle.setposition(-100,0)
the_turtle.pendown()
the_turtle.setposition(0,250)
the_turtle.setposition(100,0)
the_turtle.penup()
the_turtle.setposition(-64,90)
the_turtle.pendown()
the_turtle.setposition(64,90)